<?php
// initialisation d'une variable nommée `$i` qui servira d'index / compteur
// la boucle for se terminera quand `$i` vaudra 10
// la variable `$i` est incrémentée de 1 à chaque tour
for ($i = 0; $i < 10; $i++) {
    // à chaque tour on affiche la valeur de `$i`
    echo $i;
    echo PHP_EOL;
}

